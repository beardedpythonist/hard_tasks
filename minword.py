# Самое редкое слово
# На вход программе подается строка текста. Напишите программу, которая выводит слово, которое
# встречается реже всего, без учета регистра. Если таких слов несколько, выведите то, которое
# меньше в лексикографическом порядке.
# Формат входных данных
# На вход программе подается строка текста.
# Формат выходных данных:  Программа должна вывести слово (в нижнем регистре), встречаемое реже всего.
# Примечание 1. Программа не должна быть чувствительной к регистру, слова apple и Apple должна воспринимать
# как одинаковые.
# Примечание 2. Слово — последовательность букв. Кроме слов в тексте могут присутствовать пробелы
# и знаки препинания .,!?:;-, которые нужно игнорировать. Других символов в тексте нет.
#
# Тестовые данные
# Sample Input 1:   home sweet home
# Sample Output 1:  sweet




s = str(input())
ls = [word.strip('.,!?:;-') for word in s.lower().split()]
result = {}
for word in ls:
        result[word] = result.get(word, 0) + 1
ls1 = []
for key, value in result.items():
    if value == min(result.values()):
        ls1.append(key)
ls1.sort()
print(ls1[0])
